		 System.out.println(p1); 
		 System.out.println(p2);
		 
		 System.out.println( " Variable String" );
		 String n = "Dayana";
		 String o = "Mora";
 		 System.out.println( n);
 		 System.out.println( o);
 		 
 		 
 		System.out.println( " Variable float" );
		 float  f = 1.5f;
